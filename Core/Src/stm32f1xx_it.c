/**
  * @brief This function handles TIM6 global interrupt.
  */
void TIM6_IRQHandler(void)
{
  /* USER CODE BEGIN TIM6_IRQn 0 */

  /* USER CODE END TIM6_IRQn 0 */
  HAL_TIM_IRQHandler(&htim6);
  /* USER CODE BEGIN TIM6_IRQn 1 */

  count++;
  count2++;
  if (count >= player1Speed){
	  if (player1Direction == 0 && player1Y >= 10){player1Y--;}
	  else if (player1Direction == 1 && player1Y <= 200){player1Y++;}
	  count = 0;
  }

  if (count2 >= player2Speed && player2Y >= 10){
	  if (player2Direction == 0){player2Y--;}
	  else if (player2Direction == 1 && player2Y <= 200){player2Y++;}
	  count2 = 0;
  }


  /* USER CODE END TIM6_IRQn 1 */
}

/**
  * @brief This function handles TIM7 global interrupt.
  */
