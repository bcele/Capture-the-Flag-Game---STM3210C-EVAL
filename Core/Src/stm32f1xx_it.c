{
  /* USER CODE BEGIN TIM7_IRQn 0 */

  /* USER CODE END TIM7_IRQn 0 */
  HAL_TIM_IRQHandler(&htim7);
  /* USER CODE BEGIN TIM7_IRQn 1 */
  switch (player1State){
  	  case 0:
  		  if (player1X <= 300){player1X++;}
  		  break;
  	  case 1:
  		  if (player1X >= 10){player1X--;}
  		  break;

  }

  switch (player2State){
  	  case 0:
  		  if (player2X <= 300){player2X++;}
  		  break;
  	  case 1:
  		  if (player2X >= 10){player2X--;}
  		  break;

  }

  /* USER CODE END TIM7_IRQn 1 */
}

